
const config = {
    port : normalizePort(process.env.PORT || '3000'),
    mongoDBurl : "",
    mongoDBPort : 27017
}

/**
 * Normalize a port into a number, string, or false.
 */

function normalizePort(val) {
  var port = parseInt(val, 10);

  if (isNaN(port)) {
    // named pipe
    return val;
  }

  if (port >= 0) {
    // port number
    return port;
  }

  return false;
}

module.exports = config;